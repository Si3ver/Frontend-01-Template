// 写一个正则表达式 匹配所有 Number 直接量

//整数
var intReg = /^-?[0-9]+$/g

// 浮点数
var FloatReg = /^[-+]?[0-9]*\.?[0-9]+$/g

// 二进制数
var binReg = /^[01]+$/

// 八进制数
var OctReg = /^[0-7]+\$/

// 十六进制数
var HexReg = /(^0x[a-f0-9]{1,2}$)|(^0X[A-F0-9]{1,2}$)|(^[A-F0-9]{1,2}$)|(^[a-f0-9]{1,2}$)/




// var reg = /^[+-]+[0-9]*\.[0-9]*$/g
